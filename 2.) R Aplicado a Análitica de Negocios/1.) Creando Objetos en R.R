#Como crear objetos en R
#R es un lenguaje orientado a objetos
#La información que manipulamos en R se estructura en forma de objetos. 
#Para trabajar con R resulta importante conocer los principales tipos de objetos 
#y sus propiedades básicas. 
#En general, cada tipo de objeto viene definido por una serie de atributos. 
#Las funciones genéricas (como por ejemplo summary o plot) reconocen estos atributos
#y llevan a cabo distintos tipos de acciones en función del tipo de objeto. 
#En el curso, aprenderemos los objetos más utilizados: 
#vectores, matrices, listas, data frames y factores

#Para generar objetos, utilizamos cualquiera de los dos "Operadores de Asignación"
#Estos son "<-" e "="
#Por ejemplo

x<-5
y=5

#Asignemos un output en la variable x
x=2*3+4
#Reemplazará el valor anterior de x por el nuevo valor resultados de la operación


