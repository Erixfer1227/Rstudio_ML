# ggplot2 es un paquete de visualización de datos para el lenguaje de programación 
# estadística R. Creado por Hadley Wickham en 2005, ggplot2 es una implementación 
# de la gramática de gráficos de Leland Wilkinson, un esquema general para la 
# visualización de datos que divide los gráficos en componentes semánticos como 
# escalas y capas. ggplot2 puede servir como reemplazo de los gráficos base en R y 
# contiene una serie de valores predeterminados para la visualización web e impresa de 
# escalas comunes. Desde 2005, ggplot2 ha aumentado su uso hasta convertirse en uno 
# de los paquetes R más populares. 

# Aprendamos a instalar ggplot

install.packages("ggplot2")

# Luego de instalarlo, pongamoslo en la librería

library(ggplot2)